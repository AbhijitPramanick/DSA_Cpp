#include<iostream>
using namespace std;
int main(){
    int a = 10, b = 4;
    cout<<"Hello world"<<endl;
    cout<<"Arithmetic Operators in C++ : "<<endl;
    cout<<"The value of a+b : "<<a+b<<endl;
    cout<<"The value of a-b : "<<a-b<<endl;
    cout<<"The value of a*b : "<<a*b<<endl;
    cout<<"The value of a/b : "<<a/b<<endl;
    cout<<"The value of a%b : "<<a%b<<endl;
    cout<<"The value of a++ : "<<a++<<endl;
    cout<<"The value of a-- : "<<a--<<endl;
    cout<<"The value of ++a : "<<++a<<endl;
    cout<<"The value of --b : "<<--b<<endl;
}